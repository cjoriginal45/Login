    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        String nombre = txt_nombre.getText();
        String contra = txt_contra.getText();
        String descri = txt_descripcion.getText();
        String rol = txt_rol.getText();

        Rol role = new Rol();
        Usuario use = new Usuario();

        if (rol.equals("Administrador")) {
            role.setDescripcion(descri);
            role.setId(1);
            role.setNombreRol(rol);
            con.crearRol(role);

            use.setContrasenia(contra);
            use.setRol(role);
            use.setNombre(nombre);
            con.crearUsuario(use);
        } else {
            role.setDescripcion(descri);
            role.setId(2);
            role.setNombreRol(rol);
            con.crearRol(role);

            use.setContrasenia(contra);
            use.setRol(role);
            use.setNombre(nombre);
            con.crearUsuario(use);
        }

        JOptionPane.showMessageDialog(null, "Guardado con exito");
    }//GEN-LAST:event_btn_guardarActionPerformed

